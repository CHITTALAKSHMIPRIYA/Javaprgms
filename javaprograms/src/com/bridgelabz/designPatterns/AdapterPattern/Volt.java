
package com.bridgelabz.designPatterns.AdapterPattern;


/**
 * this method is written to measure volts
 * @author bridgelabz
 *
 */
public class Volt {
  private int volts;
  public Volt(int v)
  {
	  this.volts=v;
  }
  public int getVolts()
  {
	  return volts;
  }
  public void setVolts()
  {
	  this.volts=volts;
  }
}
