
package com.bridgelabz.ObjectOrientedPrograms.StockReport;
public class PojoStock {
	private String stockName;
	private String stockShare;
	private String sharePrice;
	/**
	 * @return the stockName
	 */
	public String getStockName() {
		return stockName;
	}
	/**
	 * @param stockName the stockName to set
	 */
	public void setStockName(String stockName) {
		this.stockName = stockName;
	}
	/**
	 * @return the stockShare
	 */
	public String getStockShare() {
		return stockShare;
	}
	/**
	 * @param stockShare the stockShare to set
	 */
	public void setStockShare(String stockShare) {
		this.stockShare = stockShare;
	}
	/**
	 * @return the sharePrice
	 */
	public String getSharePrice() {
		return sharePrice;
	}
	/**
	 * @param sharePrice the sharePrice to set
	 */
	public void setSharePrice(String sharePrice) {
		this.sharePrice = sharePrice;
	}

}
